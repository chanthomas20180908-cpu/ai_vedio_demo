def batch_generate_characters():
    """批量生成多个真人角色图片"""
    
    # 初始化日志
    setup_logging()
    
    # 加载环境变量
    load_dotenv(dotenv_path=os.path.join(os.path.dirname(__file__), "../env/default.env"))
    api_key = os.getenv("DASHSCOPE_API_KEY")
    
    # 检查API密钥
    if not api_key:
        raise ValueError("未找到DashScope API密钥，请检查环境变量配置")
    print("=" * 70)
    print("开始批量生成真人角色图片")
    print(f"共需生成 {len(CHARACTER_PROMPTS)} 个角色")
    print("包含: 6个3D卡通风格 + 16个写实摄影风格")
    print("写实风格分类: 科学家(6) + 音乐家(3) + 政治家(3) + 艺术家(2) + 文学家(2)")
    print("=" * 70)
    print()
    print()
    
    results = []
    
    for idx, character in enumerate(CHARACTER_PROMPTS, 1):
        print(f"[{idx}/{len(CHARACTER_PROMPTS)}] 正在生成: {character['name']}")
        print("-" * 70)
        
        try:
            # 使用角色预定义的seed值和negative_prompt
            seed = character['seed']
            negative_prompt = character['negative_prompt']
            style = character['style']
            
            print(f"   风格: {style}")
            print(f"   Seed: {seed}")
            
            image_path, image_url = sync_t2i_wan_v25(
                api_key=api_key,
                prompt=character['prompt'],
                negative_prompt=negative_prompt,
                size="1024*1024",
                seed=seed,
                n=1
            )
            
            if image_url and image_path:
                print(f"✅ {character['name']} 生成成功!")
                print(f"   图片URL: {image_url}")
                print(f"   本地路径: {image_path}")
                
                results.append({
                    "name": character['name'],
                    "status": "成功",
                    "path": image_path,
                    "url": image_url
                })
                
            else:
                print(f"❌ {character['name']} 生成失败")
                results.append({
                    "name": character['name'],
                    "status": "失败",
                    "path": None,
                    "url": None
                })
        
        except Exception as e:
            print(f"❌ {character['name']} 生成出错: {str(e)}")
            results.append({
                "name": character['name'],
                "status": "出错",
                "path": None,
                "url": None,
                "error": str(e)
            })
        
        print()
        
        # 避免API调用过快，添加短暂延迟
        if idx < len(CHARACTER_PROMPTS):
            time.sleep(2)
    
    # 输出汇总结果
    print("=" * 70)
    print("生成完成！汇总结果:")
    print("=" * 70)
    
    success_count = 0
    for result in results:
        status_icon = "✅" if result['status'] == "成功" else "❌"
        print(f"{status_icon} {result['name']}: {result['status']}")
        if result['status'] == "成功":
            success_count += 1
            print(f"   路径: {result['path']}")
    
    print()
    print(f"成功: {success_count}/{len(CHARACTER_PROMPTS)}")
    
    # 询问是否打开图片
    if success_count > 0:
        print()
        print("正在自动打开成功生成的图片...")
        time.sleep(1)
        
        for result in results:
            if result['status'] == "成功" and result['path']:
                clean_path = result['path'].split('?')[0]
                if os.path.exists(clean_path):
                    webbrowser.open(f"file://{clean_path}")
                    time.sleep(0.5)  # 避免同时打开太多窗口


if __name__ == "__main__":
    batch_generate_characters()
