import json


def main(arg1: str, arg2: str):
    # 确保输入是字符串
    if not isinstance(arg1, str):
        return {"error": "Input must be a string"}

    # 找到第一个 [ 和最后一个 ] 的位置
    start = arg1.find('[')
    end = arg1.rfind(']')

    print(f"start:{start}")
    print(f"end:{end}")


    # 校验方括号是否存在且顺序正确
    if start == -1 or end == -1 or end <= start:
        return {"error": "No valid JSON array found in input"}

    # 提取中间内容
    json_str = arg1[start:end+1]

    # 尝试解析为 JSON
    data = json.loads(json_str)

    # 确保输出为列表
    if not isinstance(data, list):
        data = [data]

    return {
        "result": data
    }

